class Imigrante {
  constructor(nome, email, cpf, nacionalidade, telefone, endereco, profissao) {
    this.nome = nome
    this.email = email
    this.cpf = cpf
    this.nacionalidade = nacionalidade
    this.telefone = telefone
    this.endereco = endereco
    this.profissao = profissao
  }
}

module.exports = Imigrante